def diagonalDifference(arr):
    right = 0
    left = 0
    count = 0
    for i in arr:
        right += i[count]
        count += 1
    count = -1
    for j in arr:
        left += j[count]
        count -= 1
    return abs(right - left)



if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    arr = []

    for _ in range(n):
        arr.append(list(map(int, input().rstrip().split())))

    result = diagonalDifference(arr)

    fptr.write(str(result) + '\n')

    fptr.close()
